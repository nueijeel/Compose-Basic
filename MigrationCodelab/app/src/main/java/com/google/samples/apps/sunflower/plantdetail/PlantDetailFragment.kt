/**
 * A fragment representing a single Plant detail screen.
 */

/**
 * 기본적으로 ComposeView는 화면에서 detached 될때 리소스를 아끼기 위해 자신의 Composition을 스스로 파괴(dispose)한다.
 * 하지만 Fragment 내에서 Compose를 쓸때는 이 기본 동작이 다음의 두 문제를 일으킴
 *
 * 1. 상태 보존 문제
 * Compose 내부에 있는 텍스트 입력값이나 스크롤 위치 같은 상태값들은 Framgent의 View Lifecycle을 따라야 안전하게 저장되고 복구됨.
 * 화면에서 잠시 사라졌다고 바로 파괴되면 데이터가 날아갈 수 있음
 *
 * 2. 화면 전환 애니메이션 문제
 * Framgent 이동 시 Enter나 Exit 애니메이션이 작동할 때, 기술적으로는 뷰가 이미 detached된 상태일 수 있음.
 * 만약 기본 전략을 그대로 쓰면 애니메이션이 돌아가는 도중에 Compose 화면이 먼저 사라져버려 화면이 뚝 끊기거나 하얗게 보일 수 있음
 *
 * *** 해결책 ***
 * setViewCompositionStrategy 설정 DisposeOnViewTreeLifecycleDestroyed (onCreateView 메서드 내 setViewCompositionStrategy)
 * 뷰가 window에서 detached 될 때가 아니라 Fragment의 뷰 자체가 완전히 Destroy(LifecycleOwner 파괴)될 때 Composition이 파괴됨.
 * -> 결과 : Fragment 전환 애니메이션 중에도 Compose UI가 끝까지 안전하게 보이고, 상태값도 뷰 수명 주기에 맞춰 안정적으로 유지.
 */

class PlantDetailFragment : Fragment() {

    private val args: PlantDetailFragmentArgs by navArgs()

    private val plantDetailViewModel: PlantDetailViewModel by viewModels {
        InjectorUtils.providePlantDetailViewModelFactory(requireActivity(), args.plantId)
    }

    override fun onCreateView(
        inflater: LayoutInflater,
        container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View {
        val binding = DataBindingUtil.inflate<FragmentPlantDetailBinding>(
            inflater,
            R.layout.fragment_plant_detail,
            container,
            false
        ).apply {
            composeView.apply {
                setViewCompositionStrategy(
                    ViewCompositionStrategy.DisposeOnViewTreeLifecycleDestroyed
                )
                setContent {
                    SunflowerTheme {
                        PlantDetailDescription(plantDetailViewModel)
                    }
                }
            }
            viewModel = plantDetailViewModel
            lifecycleOwner = viewLifecycleOwner
            callback = object : Callback {
                override fun add(plant: Plant?) {
                    plant?.let {
                        hideAppBarFab(fab)
                        plantDetailViewModel.addPlantToGarden()
                        Snackbar.make(root, R.string.added_plant_to_garden, Snackbar.LENGTH_LONG)
                            .show()
                    }
                }
            }

            var isToolbarShown = false

            // scroll change listener begins at Y = 0 when image is fully collapsed
            plantDetailScrollview.setOnScrollChangeListener(
                NestedScrollView.OnScrollChangeListener { _, _, scrollY, _, _ ->

                    // User scrolled past image to height of toolbar and the title text is
                    // underneath the toolbar, so the toolbar should be shown.
                    val shouldShowToolbar = scrollY > toolbar.height

                    // The new state of the toolbar differs from the previous state; update
                    // appbar and toolbar attributes.
                    if (isToolbarShown != shouldShowToolbar) {
                        isToolbarShown = shouldShowToolbar

                        // Use shadow animator to add elevation if toolbar is shown
                        appbar.isActivated = shouldShowToolbar

                        // Show the plant name if toolbar is shown
                        toolbarLayout.isTitleEnabled = shouldShowToolbar
                    }
                }
            )

            toolbar.setNavigationOnClickListener { view ->
                view.findNavController().navigateUp()
            }

            toolbar.setOnMenuItemClickListener { item ->
                when (item.itemId) {
                    R.id.action_share -> {
                        createShareIntent()
                        true
                    }
                    else -> false
                }
            }
        }
        setHasOptionsMenu(true)

        return binding.root
    }

    // Helper function for calling a share functionality.
    // Should be used when user presses a share button/menu item.
    @Suppress("DEPRECATION")
    private fun createShareIntent() {
        val shareText = plantDetailViewModel.plant.value.let { plant ->
            if (plant == null) {
                ""
            } else {
                getString(R.string.share_text_plant, plant.name)
            }
        }
        val shareIntent = ShareCompat.IntentBuilder.from(requireActivity())
            .setText(shareText)
            .setType("text/plain")
            .createChooserIntent()
            .addFlags(Intent.FLAG_ACTIVITY_NEW_DOCUMENT or Intent.FLAG_ACTIVITY_MULTIPLE_TASK)
        startActivity(shareIntent)
    }

    // FloatingActionButtons anchored to AppBarLayouts have their visibility controlled by the scroll position.
    // We want to turn this behavior off to hide the FAB when it is clicked.
    //
    // This is adapted from Chris Banes' Stack Overflow answer: https://stackoverflow.com/a/41442923
    private fun hideAppBarFab(fab: FloatingActionButton) {
        val params = fab.layoutParams as CoordinatorLayout.LayoutParams
        val behavior = params.behavior as FloatingActionButton.Behavior
        behavior.isAutoHideEnabled = false
        fab.hide()
    }

    interface Callback {
        fun add(plant: Plant?)
    }
}
